import fs from "node:fs";
import os from "node:os";
import path from "node:path";
import type { OpenCarapaceConfig } from "./types";
import { expandHomePath } from "./path";

const DEFAULT_CONFIG_RELATIVE = ".config/opencarapace/config.toml";

function isRecord(value: unknown): value is Record<string, unknown> {
  return typeof value === "object" && value !== null && !Array.isArray(value);
}

function isTomlScalar(value: unknown): value is string | number | boolean {
  return typeof value === "string" || typeof value === "number" || typeof value === "boolean";
}

function isTomlArray(value: unknown): value is Array<string | number | boolean> {
  return Array.isArray(value) && value.every((entry) => isTomlScalar(entry));
}

function toTomlValue(value: string | number | boolean | Array<string | number | boolean>): string {
  if (Array.isArray(value)) {
    return `[${value.map((entry) => toTomlValue(entry)).join(", ")}]`;
  }
  if (typeof value === "string") {
    return JSON.stringify(value);
  }
  if (typeof value === "boolean") {
    return value ? "true" : "false";
  }
  return String(value);
}

function emitTable(lines: string[], tablePath: string[], value: Record<string, unknown>): void {
  const scalarEntries: Array<{ key: string; value: string | number | boolean | Array<string | number | boolean> }> = [];
  const objectEntries: Array<{ key: string; value: Record<string, unknown> }> = [];

  const keys = Object.keys(value).sort((left, right) => left.localeCompare(right));
  for (const key of keys) {
    const entry = value[key];
    if (entry === undefined || entry === null) {
      continue;
    }
    if (isTomlScalar(entry) || isTomlArray(entry)) {
      scalarEntries.push({ key, value: entry });
      continue;
    }
    if (isRecord(entry)) {
      objectEntries.push({ key, value: entry });
    }
  }

  if (tablePath.length > 0 && (scalarEntries.length > 0 || objectEntries.length > 0)) {
    lines.push(`[${tablePath.join(".")}]`);
  }
  for (const entry of scalarEntries) {
    lines.push(`${entry.key} = ${toTomlValue(entry.value)}`);
  }
  if (tablePath.length > 0 && (scalarEntries.length > 0 || objectEntries.length > 0)) {
    lines.push("");
  }

  for (const entry of objectEntries) {
    emitTable(lines, [...tablePath, entry.key], entry.value);
  }
}

function safeParseToml(input: string): unknown {
  if (typeof Bun === "undefined" || !Bun.TOML) {
    throw new Error("TOML parsing requires Bun runtime.");
  }
  return Bun.TOML.parse(input);
}

export function resolveOpenCarapaceConfigPath(explicitPath?: string): string {
  const override = explicitPath?.trim();
  if (override) {
    return path.resolve(expandHomePath(override));
  }
  return path.resolve(os.homedir(), DEFAULT_CONFIG_RELATIVE);
}

