export function renderOpenCarapaceConfigToml(config: OpenCarapaceConfig): string {
  const lines: string[] = [
    "# OpenCarapace configuration",
    "# Generated by `opencarapace config init|tui|set`",
    "",
  ];
  emitTable(lines, [], config as Record<string, unknown>);
  while (lines.length > 0 && lines[lines.length - 1] === "") {
    lines.pop();
  }
  lines.push("");
  return lines.join("\n");
}

export function loadOpenCarapaceConfig(params?: {
  path?: string;
  strict?: boolean;
}): OpenCarapaceConfig {
  const filePath = resolveOpenCarapaceConfigPath(params?.path);
  if (!fs.existsSync(filePath)) {
    return {};
  }
  const raw = fs.readFileSync(filePath, "utf-8");
  if (!raw.trim()) {
    return {};
  }
  try {
    const parsed = safeParseToml(raw);
    if (!isRecord(parsed)) {
      return {};
    }
    return parsed as OpenCarapaceConfig;
  } catch (error) {
    if (params?.strict === false) {
      return {};
    }
    throw new Error(
      `failed to parse config at ${filePath}: ${error instanceof Error ? error.message : String(error)}`,
    );
  }
}

export function saveOpenCarapaceConfig(config: OpenCarapaceConfig, params?: { path?: string }): string {
  const filePath = resolveOpenCarapaceConfigPath(params?.path);
  fs.mkdirSync(path.dirname(filePath), { recursive: true });
  fs.writeFileSync(filePath, renderOpenCarapaceConfigToml(config), "utf-8");
  return filePath;
}

export function ensureOpenCarapaceConfig(params?: { path?: string }): {
  filePath: string;
  created: boolean;
  config: OpenCarapaceConfig;
} {
  const filePath = resolveOpenCarapaceConfigPath(params?.path);
  if (fs.existsSync(filePath)) {
    return {
      filePath,
      created: false,
      config: loadOpenCarapaceConfig({ path: filePath }),
    };
  }
  const config = defaultOpenCarapaceConfig();
  saveOpenCarapaceConfig(config, { path: filePath });
  return {
    filePath,
    created: true,
    config,
  };
}

export function setConfigValueByPath(
  config: OpenCarapaceConfig,
  keyPath: string,
  value: unknown,
): OpenCarapaceConfig {
  const keys = keyPath
    .split(".")
    .map((item) => item.trim())
    .filter(Boolean);
  if (keys.length === 0) {
    throw new Error("config key path is empty");
  }

  let cursor: Record<string, unknown> = config as Record<string, unknown>;
  for (let i = 0; i < keys.length - 1; i += 1) {
    const key = keys[i];
    if (!key) {
      continue;
    }
    const current = cursor[key];
    if (!isRecord(current)) {
      cursor[key] = {};
    }
    cursor = cursor[key] as Record<string, unknown>;
  }
  const leaf = keys[keys.length - 1];
  if (!leaf) {
    throw new Error("config key path is empty");
  }
  cursor[leaf] = value;
  return config;
}

export function parseConfigValue(raw: string): unknown {
  const value = raw.trim();
  if (!value) {
    return "";
  }
  if (value === "true") {
    return true;
  }
  if (value === "false") {
    return false;
  }
  if (value === "null") {
    return "";
  }
  if (/^-?\d+(\.\d+)?$/.test(value)) {
    return Number(value);
  }
  if (value.startsWith("[") && value.endsWith("]")) {
    try {
      const parsed = JSON.parse(value);
      if (Array.isArray(parsed)) {
        return parsed.map((entry) => String(entry));
      }
    } catch {
      // ignore json parse failure and fallback to string
    }
  }
  if (value.includes(",") && !value.includes(" ")) {
    return value
      .split(",")
      .map((item) => item.trim())
      .filter(Boolean);
  }
  return value;
}
